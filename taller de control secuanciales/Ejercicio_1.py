"""
Datos de entrada
edad uno-->e_uno-->int
edad dos-->e_dos-->int
edad tres-->e_tres-->int
Datos de salida
Promedio-->p-->float
"""
#Entradas
e_uno=int(input("Digite edad uno: "))
e_dos=int(input("Digite edad dos: "))
e_tres=int(input("Digite edad tres: "))
#Caja negra
p=(e_uno+e_dos+e_tres)/3
#Salida
print("El promedio es: ",p)

